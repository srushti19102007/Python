# Write a code to check if the number is even or odd , 
# using else-if condition and elif  condition

num = int(input("Enter a number: "))
if num % 2 == 0:
    print("The number is even...")
elif num% 2 != 0:
    print("The number is odd...")
else:
    print("Invalid input...")


